public class Pemesanan {
    protected String nama;
    protected String asal;
    protected String tujuan;
    protected double Tiket;
    protected double diskon;

    public Pemesanan(String asal, String tujuan, double Tiket) {
        this.nama = nama;
        this.asal = asal;
        this.tujuan = tujuan;
        this.Tiket = Tiket;
        this.diskon = diskon;
    }
}
